const now = new Date().toISOString();
const daysAgo = (n: number) => new Date(Date.now() - n * 86400000).toISOString();
const daysLater = (n: number) => new Date(Date.now() + n * 86400000).toISOString();

const genId = (prefix: string, i: number) => `${prefix}-${String(i).padStart(3, '0')}`;

function createSkills(prefix: string, list: Array<{ name: string; level: number; category: string }>): Skill[] {
  return list.map((s, i) => ({
    id: `${prefix}-skill-${i + 1}`,
    name: s.name,
    level: s.level,
    category: s.category,
  }));
}

function createInterviewQuestions(prefix: string, list: Array<{ category: QuestionCategory; question: string; myAnswer?: string; selfRating?: number; interviewerFeedback?: string }>): InterviewQuestion[] {
  return list.map((q, i) => ({
    id: `${prefix}-q-${i + 1}`,
    category: q.category,
    question: q.question,
    myAnswer: q.myAnswer,
    selfRating: q.selfRating,
    interviewerFeedback: q.interviewerFeedback,
  }));
}

